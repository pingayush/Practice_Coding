#Empty Tuple
my_tuple = ()
print(my_tuple)

#Tuple having Integer
my_tuple_1 = (1,2,3)
print(my_tuple_1)

#Tuple with mixed database
my_tuple_2 = (1,"Hello",3,4)
print(my_tuple_2)

#nested tuple
my_tuple_3 = ("mouse",[8,4,6],(1,2,3))
print(my_tuple_3)

#Unpacking

my_tuple = 3,4.6,"dog"
print(my_tuple)
# tuple unpacking is also possible
a, b, c = my_tuple
print(a)
print(b)
print(c)




